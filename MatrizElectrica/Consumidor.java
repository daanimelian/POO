package MatrizElectrica;

public class Consumidor {
    String nombre;
    int consumo;

    public Consumidor(String nombre, int consumo) {
        this.nombre = nombre;
        this.consumo = consumo;
    }

    public String getNombre() {
        return nombre;
    }

    public int getConsumo() {
        return consumo;
    }




}
